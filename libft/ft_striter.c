#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	size;
	size_t	i;

	if (s && f)
	{
		size = ft_strlen(s);
		i = 0;
		if (s)
		{
			while (i < size)
			{
				f(s);
				i++;
				s++;
			}
		}
	}
}
