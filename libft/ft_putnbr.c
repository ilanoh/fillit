#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n >= 0 && n < 10)
		ft_putchar(n + 48);
	if (n > 9 && n <= 2147483647)
	{
		ft_putnbr(n / 10);
		c = n % 10 + 48;
		ft_putchar(c);
	}
	if (n < 0 && n > -2147483648)
	{
		n = -n;
		ft_putchar(45);
		ft_putnbr(n);
	}
	if (n == -2147483648)
	{
		ft_putchar(45);
		ft_putchar(50);
		ft_putnbr(147483648);
	}
}
