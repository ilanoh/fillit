#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tab;
	size_t			i;

	tab = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		tab[i] = '\0';
		i++;
	}
}
